lista = [4,1,7,42,15]

#MAX: Encontrar el número mayor:
numero_mas_alto = max(lista)
print(numero_mas_alto)

#MIN: Encontrar el número menor:
numero_mas_bajo = min(lista)
print(numero_mas_bajo)

#ROUND: Redondear:
numero_con_decimales = 9.9238472
print(round(numero_con_decimales,2)) # el ",2" hace que las decimales se reduzcan a 2.

#BOOL: Retorna False: 0, vacío, none. false / Retorna True: !=0, True, Cadena, datos no vacíos.
resultado_bool = bool("asdas")
print(resultado_bool)

#ALL: Retorna True si todos los datos son verdaderos
resultado_all = all([1, True, [19238.3]])
print(resultado_all)

#SUM: Retorna el resultado de la suma de los números
suma = sum(lista)
print (suma)

